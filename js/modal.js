// EcoBridge Modal Slides using numbers abc

var slideIndex1 = 1;
    showDivs1(slideIndex1);

function plusDivs1(a) {
    showDivs1(slideIndex1 += a);
}

function showDivs1(a) {
    var b;
    var c = document.getElementsByClassName("eco-bridge-slides");
    if (a > c.length) {slideIndex1 = 1} 
    if (a < 1) {slideIndex1 = c.length} ;
    for (b = 0; b < c.length; b++) {
        c[b].style.display = "none"; 
    }
    c[slideIndex1-1].style.display = "block"; 
}



